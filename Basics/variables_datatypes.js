// VARIABLES
// We can create variables using let, var & const keywords
var x = 10;
var y = 100;
var z = 200;

// The 'let' keyword provides better scoping and helps prevent some common programming mistakes
let w = 100;
let q = "welcome";
let t = 23;

const o = "orange"; // a value that can't be changed

