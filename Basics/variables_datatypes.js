// JavaScript is a dynamically types language, so we don't explicitly have to declare the data type

// PRIMITIVE - number (whole and decimal), string, boolean, null, undefined
// NON-PRIMITIVE - object, array, function, date, regex

console.log(typeof x); // find the data type of a variable

