// a simple return function
function printGreetings() {
  return "Good morning!";
}

const greet = printGreetings();
console.log(greet);

// using the 'return' keyword

function addNumbers(numberOne, numberTwo) {
  return numberOne + numberTwo;
}

const total = addNumbers(3, 4); // store return value
console.log(total); // print return value
